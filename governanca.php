  <script>
    document.addEventListener('DOMContentLoaded', function() {
        // Variáveis de Elementos
        const toggleButton = document.getElementById('btn-ver-mais');
        const grupoTrabalho = document.getElementById('grupo-trabalho');
        const voluntarios = document.getElementById('voluntarios');
        const btnGrupoTrabalho = document.getElementById('btnGrupoTrabalho');
        const btnVoluntarios = document.getElementById('btnVoluntarios');

        /**
         * Alterna a visibilidade das seções e o texto do botão principal.
         * @param {boolean} forceVisible - Se true, força a exibição do conteúdo.
         * @param {HTMLElement} scrollToElement - Elemento para rolar se estiver sendo exibido.
         */
        function toggleContent(forceVisible = false, scrollToElement = null) {
            const isVisible = !grupoTrabalho.classList.contains('d-none');
            
            // Lógica principal de toggle: 
            // 1. Se forceVisible é TRUE, garante que d-none é REMOVIDA.
            // 2. Se forceVisible é FALSE, usa o toggle normal (adiciona/remove).
            const shouldBeVisible = forceVisible ? true : !isVisible;

            if (shouldBeVisible) {
                // EXIBIR CONTEÚDO
                grupoTrabalho.classList.remove('d-none');
                voluntarios.classList.remove('d-none');
                
                if (toggleButton) {
                    toggleButton.textContent = 'VER MENOS';
                }
                
                // Rola para o elemento, se especificado
                if (scrollToElement) {
                    scrollToElement.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
                
            } else {
                // OCULTAR CONTEÚDO
                grupoTrabalho.classList.add('d-none');
                voluntarios.classList.add('d-none');
                
                if (toggleButton) {
                    toggleButton.textContent = 'VER MAIS';
                }
            }
        }


        // --- AÇÃO 1: BOTÃO PRINCIPAL (VER MAIS / VER MENOS) ---
        if (toggleButton && grupoTrabalho && voluntarios) {
            toggleButton.addEventListener('click', function() {
                // Não precisa de rolagem forçada, basta alternar o estado atual.
                toggleContent(false);
            });
        }

        // --- AÇÃO 2: LINKS DE NAVEGAÇÃO RÁPIDA ("Saiba mais") ---
        
        // Se clicar no link do Grupo de Trabalho:
        if (btnGrupoTrabalho && grupoTrabalho) {
            btnGrupoTrabalho.addEventListener('click', function(e) {
                e.preventDefault(); 
                // Força a exibição do conteúdo e rola para o Grupo de Trabalho
                toggleContent(true, grupoTrabalho); 
            });
        }
        
        // Se clicar no link dos Voluntários:
        if (btnVoluntarios && voluntarios) {
            btnVoluntarios.addEventListener('click', function(e) {
                e.preventDefault(); 
                // Força a exibição do conteúdo e rola para os Voluntários
                toggleContent(true, voluntarios); 
            });
        }
    });
</script>
  
