  <section class = "exp-texto" id = "justificativa">
    <div class = "container w-75">
      <div class = "row">
        <h2 class = "fw-bold">Justificativa</h2>

        <p>
          O principal instrumento para discutir uma sociedade que tenha a paz como eixo estruturante foi a Declaração Universal dos Direitos Humanos (1948) e seus desdobramentos. 
          Diálogo que sempre acompanhou a humanidade, a exemplo da Declaração dos Direitos do Homem e do Cidadão de 1789.
        </p>
        <p>
          Em 1997, a Assembleia Geral das Nações Unidas proclamou o ano 2000 como o “Ano Internacional da Cultura de Paz”, e a década conseguinte ficaria reconhecida como “Década Internacional 
          para uma Cultura de Paz e Não Violência para as Crianças do Mundo”.
        </p>
        <p>
          A sociedade vem demandando movimentos conjuntos para uma cultura de paz há décadas, assim como ações pautadas em situações reais importantes para o desenvolvimento da sociedade, 
          partindo de critérios objetivos e sistemáticos que visem à implementação de programas e de projetos concretos e com real contribuição para o fortalecimento e para a difusão de uma 
          cultura de paz no seu mais amplo sentido.
        </p>
        <p>
          Não apenas na díade “Paz x Violência”, mas nas diversas relações com o Planeta, com os povos, com os animais e com o meio ambiente. Outrossim, as circunstâncias e as condições nas quais a 
          sociedade vive e os desafios que enfrenta apontam indubitavelmente para contextos gradualmente mais complexos, os quais solicitam cada vez mais a participação das instituições de ensino superior, 
          que, por essência, são espaços de produção de conhecimentos, fortalecimentos comunitários e promoção de direitos humanos – para que liderem movimentos que valorizam o diálogo e a diversidade social 
          e que contribuam para a realização de ações importantes nos diversos campos do conhecimento e das esferas da vida humana, tendo como agente integrado e de apoio as iniciativas dos governos e do Estado 
          no sentido de sinergia para a manifestação da cultura de paz e o desenvolvimento de uma sociedade justa e sustentável.
        </p>
        <p>
          Acredita-se que por meio de uma conexão e da competência vocacionada e consolidada das instituições de ensino superior, em nível regional, estadual, nacional e internacional, será possível implantar 
          e fortalecer o “Fórum de Universidades pela Paz (FOUP)” como um mecanismo eficiente para estabelecer discussões sistemáticas, estabelecer e atuar em objetivos estratégicos e fortalecer a produção de 
          conhecimentos sobre a temática da Paz, com observância das perspectivas sociológicas, antropológicas, históricas, psicológicas, linguísticas, culturais, comportamentais e legais. E que, em médio prazo, 
          as ações do FOUP, por meio das instituições signatárias, reverberarão na produção de conhecimentos e na execução de programas de promoção da cultura de paz, liderados pelas Instituições de Ensino Superior, 
          de outras instituições e por agentes governamentais.
        </p>
        <p>
          Contudo, para ter os efeitos desejados, é necessário que o FOUP seja um espaço participativo e democrático, onde cada instituição e indivíduo compreendam e contribuam para o coletivo social como um fruto do 
          individual. A Paz (no Fórum) só será alcançada se os sujeitos e os grupos forem tolerantes, desprovidos de preconceitos e de atitudes discriminatórias, conscientes e sensíveis à alteridade.
        </p>
        <p>
            Considerando que a temática “Paz” é complexa e tem amplitude global, é necessário que o FOUP promova relações com o dissenso, as diferenças e o olhar para a realidade de uma forma mais global, 
          bem como com o entendimento da dinâmica que esse tema exige. Para manter essa coerência e evitar dissonâncias, este projeto não traz conceitos de Paz, pois eles serão um trabalho para os grupos e comitês 
          temáticos que serão formados a partir do FOUP e que, certamente, serão compostos por muitas referências positivas, as quais contribuirão para o fortalecimento do consenso acerca da cultura dessa cultura.
        </p>
      </div>
    </div>
